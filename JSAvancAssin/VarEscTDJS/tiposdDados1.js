// Tipos de Dados

/* Boolean

    True & False

*/

const nome = 'banana'; // '' - para vazio

if (nome) {
    console.log('preenchido!');
}

if (!nome) {
    console.log('vazio!');
}

/* Null

    Ausência de valor

*/

const x = null;

console.log(x);

/* Undefined

    Ausência de declaração/atribuíção

*/

const y = { nome: 'Noemy' };

